void outputLvlArray(int arr[][50], int size)
{
	for (int i = 0; i < size; i++)
	{
		for (int j = 0; j < size; j++)
		{
			if (arr[i][j] == USER_NUM)
			{
				cout << USER_SYMB << USER_SYMB;
			}
			else if (arr[i][j] == ROAD_NUM)
			{
				cout << ROAD_SYMB << ROAD_SYMB;
			}
			else if (arr[i][j] == WALL_NUM)
			{
				cout << WALL_SYMB << WALL_SYMB;
			}
			else if (arr[i][j] == 3)
			{
				cout << CLEF1 << CLEF2;
			}
			else if (arr[i][j] == 4)
			{
				cout << TRAP << TRAP;
			}
		}
		cout << endl;
	}
}

void playerMovement(Key direction, int field[][SIZE]) {
	switch (direction)
	{
	case UP:
		break;
	case DOWN:
		if (posI + 1 < SIZE && field[posI + 1][posJ] != WALL_NUM) {
			swap(field[posI][posJ], field[posI + 1][posJ]);
			posI++;
		}
		break;
	case LEFT:
		break;
	case RIGHT:
		break;
	default:
		break;
	}
}

Key getDirection(int key) {
	Key direction;
	switch (key)
	{
	case UP:
		direction = UP;
		break;
	case DOWN:
		direction = DOWN;
		break;
	case LEFT:
		direction = LEFT;
		break;
	case RIGHT:
		direction = RIGHT;
		break;
	default:
		direction = ARROW;
	}
	return direction;
}

void initPlayerPosition(int field[][SIZE]) {
	for (int i = 0; i < SIZE; i++)
	{
		for (int j = 0; j < SIZE; j++)
		{
			if (field[i][j] == USER_NUM) {
				posI = i;
				posJ = j;
				return;
			}
		}
	}
}

